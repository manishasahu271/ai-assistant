# LLM Bug Report Generator
# ─────────────────────────────────────────────

def generate_bug_ticket_from_analysis(
    error_pattern: str,
    error_count: int,
    associated_commands: str,
    log_source: str,
    llm_model,
) -> BugTicket:
    prompt = f"""
    Analyze this error from CI/CD logs and generate a structured bug report.

    Error Pattern: {error_pattern}
    Occurrences: {error_count}
    Associated Commands: {associated_commands}
    Log Source: {log_source}

    Respond ONLY in valid JSON with these exact keys:
    {{
        "title": "concise bug title under 80 chars",
        "description": "detailed description of the issue",
        "severity": "Critical or High or Medium or Low",
        "failure_type": "environment or code_defect or flaky_test or data_issue",
        "steps_to_reproduce": "step by step reproduction",
        "suggested_assignee": "team or role suggestion like Backend Team or DevOps Team"
    }}
    """

    response = llm_model.invoke(prompt)
    raw = response.content.replace("```json", "").replace("```", "").strip()
    data = json.loads(raw)

    return BugTicket(
        title=data["title"],
        description=data["description"],
        severity=data["severity"],
        failure_type=data["failure_type"],
        steps_to_reproduce=data["steps_to_reproduce"],
        suggested_assignee=data["suggested_assignee"],
        error_pattern=error_pattern,
        log_source=log_source,
    )


