@dataclass
class BugTicket:
    title: str
    description: str
    severity: str
    failure_type: str
    steps_to_reproduce: str
    suggested_assignee: str
    error_pattern: str
    log_source: str
    status: str = "pending_approval"
    ticket_id: Optional[str] = None
    approval_token: Optional[str] = None

    def to_dict(self) -> Dict:
        return asdict(self)

    def generate_token(self) -> str:
        raw = f"{self.title}{self.error_pattern}{time.time()}"
        self.approval_token = hashlib.sha256(raw.encode()).hexdigest()[:16]
        return self.approval_token


