class JiraClient:
    def __init__(self):
        self.client = JIRA(
            server=JIRA_SERVER,
            basic_auth=(JIRA_EMAIL, JIRA_API_TOKEN)
        )

    def create_bug(self, ticket: BugTicket) -> str:
        severity_map = {
            "Critical": "Highest",
            "High": "High",
            "Medium": "Medium",
            "Low": "Low"
        }

        description = (
            f"*Failure Type:* {ticket.failure_type}\n"
            f"*Severity:* {ticket.severity}\n"
            f"*Error Pattern:*\n{{code}}{ticket.error_pattern}{{code}}\n\n"
            f"*Description:*\n{ticket.description}\n\n"
            f"*Steps to Reproduce:*\n{ticket.steps_to_reproduce}\n\n"
            f"*Log Source:* {ticket.log_source}\n\n"
            f"----\n"
            f"_Auto-generated by AI Log Analyzer & QA Intelligence Platform_"
        )

        fields = {
            "project": {"key": JIRA_PROJECT_KEY},
            "summary": ticket.title,
            "description": description,
            "issuetype": {"name": "Task"},
            "labels": ["ai-generated", ticket.failure_type.replace(" ", "-")],
        }

        # Try to assign the ticket
        if ticket.suggested_assignee:
            try:
                users = self.client.search_users(query=ticket.suggested_assignee)
                if users:
                    fields["assignee"] = {"accountId": users[0].accountId}
            except Exception:
                pass

        issue = self.client.create_issue(fields=fields)
        ticket.ticket_id = issue.key
        ticket.status = "created"
        return issue.key

    def get_project_members(self) -> list:
        try:
            members = self.client.search_assignable_users_for_projects(
                "", JIRA_PROJECT_KEY
            )
            return [m.displayName for m in members]
        except Exception:
            return []


