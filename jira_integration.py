# Persistent Ticket Store (JSON file)
# ─────────────────────────────────────────────

TICKET_STORE_FILE = os.path.join(os.path.dirname(__file__), "pending_tickets.json")


class TicketStore:
    def __init__(self, filepath: str = TICKET_STORE_FILE):
        self.filepath = filepath
        self._ensure_file()

    def _ensure_file(self):
        if not os.path.exists(self.filepath):
            with open(self.filepath, "w") as f:
                json.dump({}, f)

    def _load(self) -> Dict[str, dict]:
        try:
            with open(self.filepath, "r") as f:
                return json.load(f)
        except (json.JSONDecodeError, FileNotFoundError):
            return {}

    def _save(self, data: Dict[str, dict]):
        with open(self.filepath, "w") as f:
            json.dump(data, f, indent=2)

    def add(self, ticket: BugTicket) -> str:
        token = ticket.generate_token()
        data = self._load()
        data[token] = ticket.to_dict()
        self._save(data)
        return token

    def get(self, token: str) -> Optional[BugTicket]:
        data = self._load()
        entry = data.get(token)
        if entry:
            return BugTicket(**entry)
        return None

    def approve(self, token: str) -> Optional[BugTicket]:
        data = self._load()
        entry = data.get(token)
        if entry:
            entry["status"] = "approved"
            self._save(data)
            return BugTicket(**entry)
        return None

    def reject(self, token: str) -> Optional[BugTicket]:
        data = self._load()
        entry = data.get(token)
        if entry:
            entry["status"] = "rejected"
            del data[token]
            self._save(data)
            return BugTicket(**entry)
        return None

    @property
    def pending(self) -> Dict[str, BugTicket]:
        data = self._load()
        return {
            token: BugTicket(**entry)
            for token, entry in data.items()
            if entry.get("status") == "pending_approval"
        }


ticket_store = TicketStore()